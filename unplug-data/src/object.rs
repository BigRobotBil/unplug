#[derive(Debug)]
pub struct ObjectDefinition {
    pub id: ObjectId,
    pub class: ObjectClass,
    pub data: u16,
    pub path: &'static str,
}

#[derive(Debug, Copy, Clone, PartialEq, Eq)]
